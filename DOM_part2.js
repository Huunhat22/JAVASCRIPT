/* select option */
var listselectyear = Array.from(document.querySelectorAll('select[name ="Year"]'));

for (var index = 0; index < listselectyear.length; index++) {
    var element = listselectyear[index];
    console.log("select = ", element);

    for (var year = 1990; year < 1998; year++) {
        /* tao ra mot the option moi
            set attribute value cho no <option value="1990"></option>
            set innerText cho option 
            tao ra the option moi*/
        var option = document.createElement('option');
        option.setAttribute('value', year);
        option.innerText = 'Nam ' + year;
        // hàm append cho phep chèn nội dụng vào phía sau, hoặc chèn nội dung vào phần tử
        element.append(option);
        console.log(option);
    }
}



